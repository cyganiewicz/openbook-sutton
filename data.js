// Aggregate by category for charts
function getByCategoryTotals(depts) {
  const totals = {};
  depts.forEach(d => {
    const cat = d.category;
    const amt = d.fy27_request || d.fy26_approved;
    totals[cat] = (totals[cat] || 0) + amt;
  });
  return totals;
}

// Submission status counts for TM dashboard
function getSubmissionStats(depts) {
  const counts = { not_started: 0, submitted: 0, under_review: 0, approved: 0, draft: 0 };
  depts.forEach(d => { counts[d.request_status] = (counts[d.request_status] || 0) + 1; });
  return counts;
}

function fmt(n) {
  if (n >= 1000000) return `$${(n/1000000).toFixed(2)}M`;
  if (n >= 1000) return `$${(n/1000).toFixed(0)}K`;
  return `$${n.toLocaleString()}`;
}

function fmtFull(n) {
  return `$${n.toLocaleString()}`;
}

